// 문자열을 보관하고 있다가 화면에 이쁘게 출력하는 기능.
class TextView
{
	// font name, font size, color 등 다양한 정보를 관리하는 기능
	std::string data;
public:
	TextView(const std::string& s) : data(s) {}

	void show() { std::cout << data << std::endl; }
};

